List Of Commands : 

** MOVING :
 
- Win + RIGHT : moves the window to the right
- Win + LEFT  : moves the window to the left
- Win + UP    : moves the window up
- Win + DOWN  : moves the window down

** RESIZING :

- Win + ALT_L + RIGHT: enlarges the window to the right
- Win + ALT_L + LEFT : narrows the window to the left
- Win + ALT_L + DOWN : enlarges the window down
- Win + ALT_L + UP   : narrows the window up

** SIMPLE TILING :

- Win + d : move the window on the right 
- Win + q : move the window on the left
- Win + s : move the window on the bottom
- Win + z : move the window on the top
- Win + f : move the window on full screen.

** REAL TILING :

- Win + 1 : Tiles in square
- Win + 2 : Tiles in square + move the main window on top.
- Win + 3 : Tiles in vertical
- Win + 4 : Tiles in horizontal
- Win + 5 : Tiles in horizontal + move the main window on left.
- Win + 6 : Tiles in vertical + move the main window on top. 

** RESIZIN WHILE TILING : ( <!> some bugs ... )

- Win + ALT_L + RIGHT: enlarges the window to the right
- Win + ALT_L + LEFT : narrows the window to the left
- Win + ALT_L + DOWN : enlarges the window down
- Win + ALT_L + UP   : narrows the window up

** OTHER ACTIONS : 

- Win + c : close the window (be carreful with Firefox & Thunderbird)
- Win + m : Minimize the Window
- Win + t : minimize or unminimize terminal on workspace.

- Win + b : focus last window
- Win + n : focus next window

** RECOMMENDATIONS : 

